//                                          Operadores Aritméticos

//Soma
function soma(num1,num2){
    return num1+num2
};
console.log(`Soma: ${soma(2,3)}`);

//Subtração
function subtração(num1,num2){
    return num1-num2
};
console.log(`Subtração: ${subtração(10,3)}`);

//Multiplicaçã0
function multi(num1,num2){
    return num1*num2
};
console.log(`Multiplicação: ${multi(5,5)}`);

//Divisão 
function div(num1,num2){
    return num1/num2
};
console.log(`Divisão: ${div(12,2)}`);
//Resto da divisão
function restoDiv(num1,num2){
    return num1+num2
};
console.log(`Resto da divisão: ${restoDiv(3,2)}`);

//Incremento
function Incremento(num1){
    return  num1+=10
};
console.log(`Incremento: ${Incremento(10)}`);

//Decremento
function decremento(num1){
    return num1-=3
};
console.log(`Decremento: ${decremento(20)}`);

//Potenciação
function potencia(num1,num2){
    return num1**num2
};
console.log(`Potência: ${potencia(2,3)}`);