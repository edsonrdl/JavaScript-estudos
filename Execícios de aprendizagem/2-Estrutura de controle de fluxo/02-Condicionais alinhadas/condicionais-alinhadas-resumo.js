// if...else if...else
// O if...else if...else é uma estrutura condicional que permite testar várias condições em sequência e executar diferentes blocos de código, dependendo do resultado de cada teste.
// Sintaxe:
// if (condição1) {
//      bloco de código a ser executado se a condição1 for verdadeira
//   } else if (condição2) {
//      bloco de código a ser executado se a condição1 for falsa e a condição2 for verdadeira
//   } else {
//      bloco de código a ser executado se todas as condições forem falsas
//   }
  
//ex
let x = 10;
if (x > 10) {
  console.log("x é maior do que 10");
} else if (x < 10) {
  console.log("x é menor do que 10");
} else {
  console.log("x é igual a 10");
}
