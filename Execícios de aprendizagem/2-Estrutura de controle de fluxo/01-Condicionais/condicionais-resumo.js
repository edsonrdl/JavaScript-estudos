// if (se)
// O if é a estrutura condicional mais básica em JavaScript. Ela executa um bloco de código somente se a condição especificada for verdadeira.
// Sintaxe:
// if (condição) {
  // bloco de código a ser executado se a condição for verdadeira
//}

//ex
let x = 10;
if (x > 5) {
  console.log("x é maior do que 5");
};


// if(se)...else(Senão)  
// O if...else é uma estrutura condicional que permite executar um bloco de código se a condição especificada for verdadeira e outro bloco de código se a condição for falsa.
// Sintaxe:
// if (condição) {
//   // bloco de código a ser executado se a condição for verdadeira
// } else {
//   // bloco de código a ser executado se a condição for falsa
// }

//ex
let y = 10;
if (y > 5) {
  console.log("y é maior do que 5");
} else {
  console.log("y é menor ou igual a 5");
};

