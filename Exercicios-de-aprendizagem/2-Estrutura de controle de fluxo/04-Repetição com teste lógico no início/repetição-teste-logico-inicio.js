// while(enquanto)   faz o teste lógico no início
// O laço while executa um bloco de código enquanto uma condição especificada for verdadeira.
// Sintaxe:
// while (condição) {
//    bloco de código a ser executado enquanto a condição for verdadeira
// }

//ex
let i = 0;
while (i < 5) {
  console.log(i);
  i++;
}
