// do(fazer)...while(enquanto)   teste lógico no final 
// O laço do...while é semelhante ao while, mas executa o bloco de código pelo menos uma vez, antes de verificar se a condição é verdadeira.
// Sintaxe:
// do {
//    bloco de código a ser executado pelo menos uma vez
// } while (condição);

//ex
let i = 0;
do {
  console.log(i);
  i++;
} while (i < 5);
